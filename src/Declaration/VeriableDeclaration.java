package Declaration;

public class VeriableDeclaration {

	public static void main(String[] args) {
		int shoes = 7;
		int bags = 5; 
		int car= 1;
		int phones = 3;
		int laptops= 4;

		System.out.println ("I had " + bags + " bags.");
		System.out.println ("I had " + shoes + " shoes.");
		System.out.println ("I have " + car + " car.");
		System.out.println ("I have " + phones + " phones.");
		System.out.println ("I have " + laptops + " laptops.");

	}

}
